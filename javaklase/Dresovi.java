public class Dresovi {
    String boja;
    String brend;

    public Dresovi() {
    }

    public Dresovi(String boja, String brend) {
        this.boja = boja;
        this.brend = brend;
    }

    public String getBoja() {
        return boja;
    }

    public String getBrend() {
        return brend;
    }

    public void setBoja(String boja) {
        this.boja = boja;
    }

    public void setBrend(String brend) {
        this.brend = brend;
    }
  
    
    
    
}
